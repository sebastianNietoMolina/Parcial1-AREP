package edu.escuelaing.arep.app;

public class Trigonometricas {

    public Trigonometricas() {
    }

    public double getTan(double num){
        return Math.tan(num);
    }

    public double getCos(double num){
        return Math.cos(num);
    }

    public double getSen(double num){
        return Math.sin(num);
    }





}
